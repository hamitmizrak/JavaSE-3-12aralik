package com.ecodation.dersler;

public class Ders010_Math {
	public static void main(String[] args) {
		// Math static bir yapıdır.
		System.out.println(Math.PI);
		System.out.println(Math.E);

		System.out.println(Math.min(7, 85));
		System.out.println(Math.max(7, 85));

		System.out.println(Math.abs(-44));
		System.out.println(Math.pow(2, 5));

		System.out.println(Math.sqrt(25));

		System.out.println(Math.floor(4.9)); // alta yuvarlama
		System.out.println(Math.ceil(4.1)); // yukarı yuvarlama
		System.out.println(Math.round(4.4));
		System.out.println(Math.round(4.5));
	}
}
